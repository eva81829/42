#include "libft.h"

size_t	ft_strlcpy(char *dst, const char *src, size_t dstsize)
{
	unsigned int	i;
	size_t			count;

	i = 0;
	count = 0;
	if (!dst || !src)
		return (0);
	while (src[count])
		count++;
	if (dstsize < 1)
		return (count);
	while (src[i] && i < dstsize - 1)
	{
		dst[i] = src[i];
		i++;
	}
	dst[i] = '\0';
	return (count);
}
