int	ft_sqrt(int nb)
{
	int	sqrt;
	int	i;

	i = 0;
	sqrt = 0;
	while (i <= nb && i <= 46340)
	{
		if (i * i == nb)
		{
			sqrt = i;
			break ;
		}
		i++;
	}
	return (sqrt);
}

